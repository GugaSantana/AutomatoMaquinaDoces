package br.usjt.tcalform.model;

public class Pilha {

	private int pilha;
	
	public Pilha(int pilha)
	{
		setPilha(pilha);
	}
	
	public Pilha()
	{
		this.pilha = 0;
	}

	public int getPilha() {
		return pilha;
	}

	public void setPilha(int pilha) {
		this.pilha = pilha;
	}
	
}
